import fs from 'fs-extra';
import path from 'path';
import { ProjectConfig, ShadcnConfig } from '../types';

export async function setupShadcnUI(config: ProjectConfig, projectPath: string) {
  // Create components.json config file
  const componentsConfig: ShadcnConfig = {
    style: 'default',
    baseColor: 'slate',
    cssVariables: true,
    rsc: config.appRouter,
    tsx: config.typescript,
    tailwind: {
      config: 'tailwind.config.js',
      css: config.appRouter ? 'src/app/globals.css' : 'src/styles/globals.css',
      baseColor: 'slate',
      cssVariables: true
    },
    aliases: {
      components: '@/components',
      utils: '@/lib/utils'
    }
  };

  await fs.writeJson(path.join(projectPath, 'components.json'), componentsConfig, { spaces: 2 });

  // Create basic UI components
  await createButtonComponent(config, projectPath);
  await createCardComponent(config, projectPath);
  await createInputComponent(config, projectPath);
  await createLabelComponent(config, projectPath);
  await createBadgeComponent(config, projectPath);
}

async function createButtonComponent(config: ProjectConfig, projectPath: string) {
  const ext = config.typescript ? 'tsx' : 'jsx';
  
  const buttonContent = `import * as React from "react"
import { Slot } from "@radix-ui/react-slot"
import { cva, type VariantProps } from "class-variance-authority"

import { cn } from "@/lib/utils"

const buttonVariants = cva(
  "inline-flex items-center justify-center whitespace-nowrap rounded-md text-sm font-medium ring-offset-background transition-colors focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50",
  {
    variants: {
      variant: {
        default: "bg-primary text-primary-foreground hover:bg-primary/90",
        destructive:
          "bg-destructive text-destructive-foreground hover:bg-destructive/90",
        outline:
          "border border-input bg-background hover:bg-accent hover:text-accent-foreground",
        secondary:
          "bg-secondary text-secondary-foreground hover:bg-secondary/80",
        ghost: "hover:bg-accent hover:text-accent-foreground",
        link: "text-primary underline-offset-4 hover:underline",
      },
      size: {
        default: "h-10 px-4 py-2",
        sm: "h-9 rounded-md px-3",
        lg: "h-11 rounded-md px-8",
        icon: "h-10 w-10",
      },
    },
    defaultVariants: {
      variant: "default",
      size: "default",
    },
  }
)

export interface ButtonProps
  extends React.ButtonHTMLAttributes<HTMLButtonElement>,
    VariantProps<typeof buttonVariants> {
  asChild?: boolean
}

const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant, size, asChild = false, ...props }, ref) => {
    const Comp = asChild ? Slot : "button"
    return (
      <Comp
        className={cn(buttonVariants({ variant, size, className }))}
        ref={ref}
        {...props}
      />
    )
  }
)
Button.displayName = "Button"

export { Button, buttonVariants }`;

  await fs.writeFile(path.join(projectPath, 'src/components/ui', `button.${ext}`), buttonContent);
}

